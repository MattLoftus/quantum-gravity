def random_walk_permutation(n, step_size, rng_local):
    """Generate a permutation via random walk mixed with identity.
    positions[i] = i + step_size * cumulative_noise[i].
    Small step_size -> nearly identity permutation (high correlation).
    Large step_size -> random permutation (walk dominates identity).
    Convert to a permutation by argsort of the positions."""
    noise = rng_local.standard_normal(n)
    cumulative = np.cumsum(noise)
    positions = np.arange(n, dtype=np.float64) + step_size * cumulative
    # Convert to permutation by ranking
    return np.argsort(positions).astype(int)

