def average_observables(obs_list):
    """Average a list of observable dicts."""
    keys = ['f', 'longest_chain', 'n_links', 'link_frac', 'fiedler', 'bd_action', 'bd_per_n']
    avg = {}
    for k in keys:
        vals = [o[k] for o in obs_list]
        avg[k] = np.mean(vals)
        avg[k + '_std'] = np.std(vals)
    return avg


