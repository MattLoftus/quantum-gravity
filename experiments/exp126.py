def sample_mallows(n, q, rng_local):
    """Sample from the Mallows distribution with parameter q.
    Uses the sequential insertion method (Mallows 1957).
    P(s) proportional to q^inv(s).
    """
    perm = [0]
    for i in range(1, n):
        # Element i can be inserted at positions 0..i
        # Position j contributes (i-j) inversions with the elements already placed
        # Weight for position j: q^(i-j)
        weights = np.array([q ** (i - j) for j in range(i + 1)], dtype=np.float64)
        weights /= weights.sum()
        j = rng_local.choice(i + 1, p=weights)
        perm.insert(j, i)
    return np.array(perm)

