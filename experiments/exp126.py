def hasse_adjacency(cs):
    """Symmetric adjacency matrix of the Hasse diagram."""
    links = cs.link_matrix()
    return (links | links.T).astype(np.float64)


def hasse_laplacian(cs):
    """Laplacian of the Hasse diagram."""
    adj = hasse_adjacency(cs)
    degree = np.sum(adj, axis=1)
    return np.diag(degree) - adj


def fiedler_value(cs):
    """Second-smallest eigenvalue of the Hasse Laplacian."""
    L = hasse_laplacian(cs)
    evals = np.sort(np.linalg.eigvalsh(L))
    if len(evals) < 2:
        return 0.0
    return float(evals[1])


def ordering_fraction(cs):
    """Fraction of pairs that are causally related."""
    N = cs.n
    if N < 2:
        return 0.0
    return cs.num_relations() / (N * (N - 1) / 2)


def longest_chain(cs):
    """Longest chain length."""
    return cs.longest_chain()


def count_links_fast(cs):
    """Number of links (covers) in the causal set."""
    return int(np.sum(cs.link_matrix()))


def hasse_diameter(cs):
    """Diameter of the Hasse diagram (longest shortest path)."""
    adj = hasse_adjacency(cs)
    N = cs.n
    dist = np.full((N, N), N + 1)
    np.fill_diagonal(dist, 0)
    for source in range(N):
        queue = [source]
        visited = {source}
        d = 0
        while queue:
            next_queue = []
            for node in queue:
                for neighbor in range(N):
                    if adj[node, neighbor] > 0 and neighbor not in visited:
                        visited.add(neighbor)
                        dist[source, neighbor] = d + 1
                        next_queue.append(neighbor)
            queue = next_queue
            d += 1
    finite = dist[dist < N + 1]
    return int(np.max(finite)) if len(finite) > 0 else 0


def antichain_width(cs):
    """Width = size of largest antichain (greedy layering)."""
    N = cs.n
    order = cs.order
    remaining = set(range(N))
    max_layer = 0
    while remaining:
        minimals = []
        for x in remaining:
            is_min = True
            for y in remaining:
                if y != x and order[y, x]:
                    is_min = False
                    break
            if is_min:
                minimals.append(x)
        if not minimals:
            break
        max_layer = max(max_layer, len(minimals))
        remaining -= set(minimals)
    return max_layer


def measure_observables(cs, label=""):
    """Compute a standard set of observables for a causal set."""
    N = cs.n
    f = ordering_fraction(cs)
    lc = longest_chain(cs)
    nl = count_links_fast(cs)
    link_frac = nl / N if N > 0 else 0
    fied = fiedler_value(cs)
    bd = bd_action_2d_fast(cs)
    bd_per_n = bd / N if N > 0 else 0
    return {
        'label': label,
        'N': N,
        'f': f,
        'longest_chain': lc,
        'n_links': nl,
        'link_frac': link_frac,
        'fiedler': fied,
        'bd_action': bd,
        'bd_per_n': bd_per_n,
    }


