def random_derangement(n, rng_local):
    """Generate a random derangement (no fixed points) via rejection."""
    for _ in range(10000):
        p = rng_local.permutation(n)
        if not np.any(p == np.arange(n)):
            return p
    # Fallback: swap any fixed point with its neighbor
    p = rng_local.permutation(n)
    for i in range(n):
        if p[i] == i:
            j = (i + 1) % n
            p[i], p[j] = p[j], p[i]
    return p

