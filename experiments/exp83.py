def reconstruct_coordinates_mds(cs, target_dim=2):
    """Reconstruct embedding coordinates from causal matrix alone via MDS."""
    dist = geodesic_distance_matrix(cs)
    # Replace infinities with large finite distance
    max_finite = dist[np.isfinite(dist)].max() if np.any(np.isfinite(dist)) else 10.0
    dist[~np.isfinite(dist)] = max_finite * 2

    mds = MDS(n_components=target_dim, dissimilarity='precomputed',
              random_state=42, max_iter=300, normalized_stress='auto')
    coords_recon = mds.fit_transform(dist)
    return coords_recon, mds.stress_


def compare_coordinates(true_coords, recon_coords):
    """
    Compare true and reconstructed coordinates.
    Use Procrustes alignment (rotation, reflection, scaling, translation).
    Returns correlation coefficient for each dimension and overall R^2.
    """
    # Center both
    tc = true_coords - true_coords.mean(axis=0)
    rc = recon_coords - recon_coords.mean(axis=0)

    # Procrustes: find optimal rotation R such that ||tc - rc @ R||^2 is minimized
    # SVD of tc.T @ rc
    ndim = tc.shape[1]
    U, S, Vt = np.linalg.svd(tc.T @ rc)
    R = Vt.T @ U.T

    # Also try with reflection
    best_corr = -1
    best_aligned = None
    for sign in [1, -1]:
        D = np.eye(ndim)
        D[-1, -1] = sign
        R_try = Vt.T @ D @ U.T
        aligned = rc @ R_try

        # Scale to match
        scale = np.sum(tc * aligned) / np.sum(aligned * aligned)
        aligned *= scale

        # Compute correlation
        corrs = []
        for d in range(tc.shape[1]):
            c, _ = stats.pearsonr(tc[:, d], aligned[:, d])
            corrs.append(abs(c))
        mean_corr = np.mean(corrs)
        if mean_corr > best_corr:
            best_corr = mean_corr
            best_aligned = aligned

    # R^2 overall
    ss_res = np.sum((tc - best_aligned) ** 2)
    ss_tot = np.sum((tc - tc.mean(axis=0)) ** 2)
    r_squared = 1 - ss_res / ss_tot

    # Per-dimension correlations
    dim_corrs = []
    for d in range(tc.shape[1]):
        c, _ = stats.pearsonr(tc[:, d], best_aligned[:, d])
        dim_corrs.append(c)

    return dim_corrs, r_squared, best_aligned


