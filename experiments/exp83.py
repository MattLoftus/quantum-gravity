def mm_dimension_fast(cs):
    """Myrheim-Meyer dimension from ordering fraction."""
    f = ordering_fraction_fast(cs)
    if f <= 0 or f >= 1:
        return float('nan')
    # f = R / C(n,2), MM uses f_d = f/2
    f_d = f / 2.0
    return _invert_ordering_fraction(f_d)


def chain_antichain_dimension(cs):
    """
    Estimate dimension from the ratio of longest chain to width.
    In d-dim Minkowski diamond with N elements:
      height ~ N^{1/d}, width ~ N^{(d-1)/d}
    So height/width ~ N^{(2-d)/d} and log(height/width)/log(N) = (2-d)/d
    => d = 2 / (1 + log(height/width)/log(N))
    """
    N = cs.n
    if N < 5:
        return float('nan')
    height = longest_chain_length(cs)
    w = width_estimate(cs)
    if height <= 0 or w <= 0:
        return float('nan')
    ratio = height / w
    log_ratio = np.log(ratio) / np.log(N)
    # d = 2 / (1 + log_ratio) but this is approximate
    if 1 + log_ratio <= 0:
        return float('nan')
    d_est = 2.0 / (1.0 + log_ratio)
    return d_est


def midpoint_dimension(cs):
    """
    Estimate dimension from interval statistics.
    For d-dim Minkowski, the mean interval size <I> for related pairs
    scales as f_d * N where f_d ~ Gamma(d+1)*Gamma(d/2)/(4*Gamma(3d/2)).
    We use the ratio of different interval sizes to extract d.
    """
    intervals = count_intervals_by_size(cs, max_size=5)
    # Ratio of I_0 (links) to total relations
    total_rel = cs.order.astype(int).sum()
    if total_rel == 0:
        return float('nan')
    n_links = intervals.get(0, 0)
    link_ratio = n_links / total_rel
    # In d dimensions: link_ratio ~ d / (d + something)
    # Empirically calibrate: in d=2, link_ratio ~ 0.4-0.5; in d=3, ~0.2-0.3; d=4, ~0.1-0.2
    # Use the ordering fraction method as backup calibration
    if link_ratio <= 0 or link_ratio >= 1:
        return float('nan')
    # Rough empirical: d ~ 2 / link_ratio (very approximate)
    # Better: use the fact that for a diamond, the fraction of intervals of size k
    # follows a known distribution that depends on d
    # For now use a hybrid approach: ordering fraction is most reliable
    return mm_dimension_fast(cs)  # fall back to MM


