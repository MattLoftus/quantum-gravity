def curvature_estimator(cs):
    """
    Estimate curvature from interval statistics.

    In flat spacetime, the number of k-element intervals follows a specific
    distribution. In curved spacetime, there's a deficit/surplus.

    The key relation (Benincasa-Dowker-Glaser):
      S_BD = sum_x [alpha_0 + alpha_1*n_1(x) + alpha_2*n_2(x) + ...]
    converges to (1/l^2) * integral(R * sqrt(-g) d^d x) in the continuum.

    So the BD action per element = average curvature * volume per element.
    """
    N = cs.n
    intervals = count_intervals_by_size(cs, max_size=4)
    n_links = intervals.get(0, 0)
    n_I2 = intervals.get(1, 0)
    n_I3 = intervals.get(2, 0)

    # 2D BD action: S = N - 2*L + I_2
    S_bd_2d = N - 2 * n_links + n_I2

    # Action per element ~ average scalar curvature * l^2
    R_est = S_bd_2d / N

    # Also compute the interval ratio diagnostic
    # In flat 2D: I_2/L should be a specific constant
    if n_links > 0:
        interval_ratio = n_I2 / n_links
    else:
        interval_ratio = float('nan')

    return {
        'S_bd': S_bd_2d,
        'R_per_element': R_est,
        'interval_ratio': interval_ratio,
        'n_links': n_links,
        'n_I2': n_I2,
        'n_I3': n_I3,
    }


