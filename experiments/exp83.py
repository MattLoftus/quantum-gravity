def hausdorff_distance(coords1, coords2):
    """Compute Hausdorff distance between two point sets."""
    from scipy.spatial.distance import cdist
    D = cdist(coords1, coords2)
    d12 = np.max(np.min(D, axis=1))
    d21 = np.max(np.min(D, axis=0))
    return max(d12, d21)


